    private void tabelsopirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelsopirMouseClicked
        // TODO add your handling code here:
        int i = tabelsopir.getSelectedRow();
        TableModel model = tabelsopir.getModel();
        txtnama.setText(model.getValueAt(i, 0).toString());
        
        String usia1 = model.getValueAt(i, 1).toString();
        switch(usia1){
            case "26":
                cmbusia.setSelectedIndex(0);
                break;
            case "30":
                cmbusia.setSelectedIndex(1);
                break;
            case "36":
                cmbusia.setSelectedIndex(2);
                break;
        }
        
        String divisi1 = model.getValueAt(i, 2).toString();
        switch(divisi1){
            case "Angkot":
                cmbdivisi.setSelectedIndex(0);
                break;
            case "Bus":
                cmbdivisi.setSelectedIndex(1);
                break;
            case "LRT":
                cmbdivisi.setSelectedIndex(2);
                break;
            case "Bajaj":
                cmbdivisi.setSelectedIndex(3);
                break;
        }
        
        
        
        txtplat.setText(model.getValueAt(i, 3).toString());
        txtsim.setText(model.getValueAt(i, 4).toString());
        txtktp.setText(model.getValueAt(i, 5).toString());
        txtalamat.setText(model.getValueAt(i, 6).toString());
        
    }//GEN-LAST:event_tabelsopirMouseClicked

