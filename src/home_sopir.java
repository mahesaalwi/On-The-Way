    private void btnsegarkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsegarkanActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "SELECT * FROM data_sopir";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel) tabelsopir.getModel();
            dtm.setRowCount(0);
            String [] data = new String[9];
            int i = 1;

            while(rs.next()) {
                data[0] = rs.getString("nis");
                data[1] = rs.getString("nama");
                data[2] = rs.getString("usia");
                data[3] = rs.getString("divisi");
                data[4] = rs.getString("plat");
                data[5] = rs.getString("sim");
                data[6] = rs.getString("ktp");
                data[7] = rs.getString("alamat");
                data[8] = rs.getString("foto");
                dtm.addRow(data);
                i++;
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnsegarkanActionPerformed

